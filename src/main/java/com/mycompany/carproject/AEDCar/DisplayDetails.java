public void populateCarDetails() {
        try {
            FileInputStream file = new FileInputStream("Car.dat");
            ObjectInputStream ipfile = new ObjectInputStream(file);
            boolean eof = false;
            while (!eof) {
                try {
                    carDetails2.add((CarDetails) ipfile.readObject());
                } catch (EOFException eoe) {
                    eof = true;
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }

//JOptionPane.showMessageDialog(null, "file size :"+carDetails2.size());
            ipfile.close();
//for(CarDetails c : carDetails2){
//}
//CarDetails c = carDetails2.get(1);
//jTextField1.setName("trisha");

        } catch (IOException ioe) {
            JOptionPane.showMessageDialog(null, ioe.getMessage());
        }

    }

